<h2>Regiões metropolitanas avançam no desenvolvimento humano e reduzem disparidades</h3>

<i>Estudo do PNUD, Ipea e Fundação João Pinheiro mostra avanços nas regiões metropolitanas, mas também revela a disparidade remanescente dentro dos municípios</i>
<br/><br/>
<p>O Instituto de Pesquisa Econômica Aplicada (Ipea), a Fundação João Pinheiro e o Programa das Nações Unidas para o Desenvolvimento (PNUD) lançaram hoje (25/11) o Atlas do Desenvolvimento Humano nas Regiões Metropolitanas Brasileiras. Parte da série Atlas do Desenvolvimento Humano no Brasil, ele é composto por um site (www.atlasbrasil.org.br) e por uma publicação.</p>
<p>Entre 2000 e 2010, as 16 Regiões Metropolitanas (RMs) pesquisadas registraram avanços no Índice de Desenvolvimento Humano Municipal (IDHM) e em todos os outros 200 indicadores socioeconômicos levantados. Atualmente, todas as regiões metropolitanas pesquisadas se encontram na faixa de Alto Desenvolvimento Humano, com IDHM acima de 0,700.  São elas: Belém, Belo Horizonte, Cuiabá, Curitiba, Distrito Federal, Fortaleza, Goiânia, Manaus, Natal, Porto Alegre, Recife, Rio de Janeiro, Salvador, São Luis, São Paulo e Vitória.</p>
<p>Mesmo com o avanço generalizado, o ritmo de crescimento entre elas não foi o mesmo. As RMs que possuíam os menores indicadores tiveram um ritmo de crescimento mais acelerado do que as que já estavam em patamares mais altos de desenvolvimento humano, o que contribuiu para a redução do hiato entre elas. 
<p>	Entre 2000 e 2010, a diferença entre a RM de IDHM mais elevado (São Paulo) e a RM de IDHM mais baixo (Manaus) caiu de 22,1% para 10,3%. Em termos de IDHM, portanto, as regiões metropolitanas estão menos desiguais em 2010 do que estavam em 2000.
<p>Apesar da reconhecida melhora e da redução das disparidades, a desigualdade dentro dos municípios ainda é um fator marcante. Em casos extremos, na mesma região metropolitana há Unidades de Desenvolvimento Humano (UDHs) – áreas menores que bairros nos territórios mais populosos e heterogêneos, mas iguais a municípios inteiros quando estes têm população insuficiente para desagregações estatísticas – com renda domiciliar per capita mensal de quase R$ 7,9 mil, enquanto em outras UDHs esse valor não chega a R$ 170, uma diferença de 45 vezes entre a UDH mais abastada e a mais carente.
<p>A esperança de vida ao nascer varia, em média, 12 anos dentro das RMs. Se consideradas todas as mais de 9 mil UDHs pesquisadas, das 16 RMs analisadas, o melhor dado corresponde a 82 anos, enquanto o mais baixo é de 67 anos. São 15 anos de diferença em termos de expectativa de vida ao nascer. 
<p>Apesar de a dimensão educação ter sido a que mais avançou, em comparação com longevidade e renda, as disparidades também se repetem aqui, como no caso da escolaridade da população adulta. Nas UDHs com melhor desempenho entre todas as 16 regiões metropolitanas, o percentual de pessoas de 18 anos ou mais de idade com ensino fundamental completo varia de 91% a 96%. Já nas UDHs com pior desempenho, a variação fica entre 21% e 37%.
<p>As desigualdades em cada região metropolitana são, muitas vezes, mascaradas pelas médias, dando a falsa impressão de que os municípios são homogêneos. A partir da análise dos dados é possível concluir que mesmo nas regiões metropolitanas mais carentes, como Manaus e Belém, há “bolsões” com muito alto desenvolvimento humano. O mesmo vale para as RMs com maior IDHM, como São Paulo e a RIDE do Distrito Federal, por exemplo, em que há várias UDHs com baixos níveis de renda e educação.
<br/><br/>
<strong>Troca de posições entre os mais altos</strong>
<br/><br/>
<p>A diferença no ritmo de evolução do IDHM nas regiões metropolitanas acarretou uma troca nas primeiras posições. Em comparação ao ano 2000, as Regiões Metropolitanas do Rio de Janeiro e de Porto Alegre deixaram de figurar entre as cinco com maior Índice de Desenvolvimento Humano. Com um ritmo de crescimento mais acelerado, entraram em seus lugares a RIDE do Distrito Federal a Região Metropolitana da Grande Vitória.
<p>Em situação oposta estão as Regiões Metropolitanas de Recife, Natal, Fortaleza, Belém e, em último lugar, Manaus, que apresenta o menor IDHM entre as Regiões Metropolitanas pesquisadas.
