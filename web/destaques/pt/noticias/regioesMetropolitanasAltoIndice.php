<h2>Regiões Metropolitanas brasileiras têm Alto Índice de Desenvolvimento Humano Municipal, mostra Atlas Brasil</h3>

<i>Atlas traz agora IDHM por mais de 9 mil áreas de 16 Regiões Metropolitanas brasileiras.</i>
<br/>
<p>
<ul>
<li>Regiões Metropolitanas de São Paulo, Brasília, Curitiba, Belo Horizonte e Vitória são as de maior Índice de Desenvolvimento Humano Municipal (IDHM) </li>
<li>Manaus, Belém, Fortaleza, Natal e Recife são as RMs com menor IDHM  </li>
<li>Entre 2000 e 2010, todos os índices e subíndices das RMs registraram avanços; a disparidade entre o maior e o menor IDHM também diminuiu –  de 22,1% para 10,3% </li>
<li>Em 2000, 39% das Unidades de Desenvolvimento Humano (áreas menores que bairros nos territórios mais populosos e heterogêneos, mas iguais a municípios inteiros quando estes têm população insuficiente para desagregações estatísticas)  das RMs tinham baixo ou muito baixo desenvolvimento humano. Em 2010, nenhuma tinha muito baixo desenvolvimento humano e apenas 2% tinham baixo desenvolvimento humano</li>
</ul>

<p> <strong>Brasília, 25 de novembro de 2014 </strong> – Dados de desenvolvimento humano de 16 Regiões Metropolitanas (RM) do país mostram redução das disparidades entre regiões do Norte e do Sul do país, entre 2000 e 2010. As informações fazem parte do novo Atlas do Desenvolvimento Humano nas Regiões Metropolitanas Brasileiras, lançado hoje (25/11), em Brasília, fruto de uma parceria entre o Programa das Nações Unidas para o Desenvolvimento (PNUD), o Instituto de Pesquisa Econômica Aplicada (Ipea) e a Fundação João Pinheiro (FJP). Os dados são calculados com base nos Censos Demográficos de 2000 e 2010, do IBGE.
<p>O Atlas do Desenvolvimento Humano nas Regiões Metropolitanas Brasileiras é uma extensão do site lançado em 2013 (www.atlasbrasil.org.br), que trazia o Índice de Desenvolvimento Humano Municipal (IDHM) de 5.565 municípios brasileiros, além de outros 200 indicadores socioeconômicos. Agora, a plataforma pretende mostrar a evolução dos indicadores entre 2000 e 2010 em 16 Regiões Metropolitanas Brasileiras, bem como evidenciar as disparidades existentes dentro das mesmas, antes omitidas pelas médias municipais. Ao olhar separadamente as Unidades de Desenvolvimento Humano (UDHs) – áreas menores que bairros nos territórios mais populosos e heterogêneos, mas iguais a municípios inteiros quando estes têm população insuficiente para desagregações estatísticas – de cada RM, é possível ver as diferenças entre elas em termos de desenvolvimento humano.
