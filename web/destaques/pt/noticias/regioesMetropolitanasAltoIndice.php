<p>O presidente do Ipea, Sergei Soares, defende a importância da adaptação da ferramenta para o nível metropolitano. “A centralidade dos espaços metropolitanos para a vida econômica, social e política do país exigiu a construção de uma versão do Atlas que incorporasse os espaços intramunicipais e intrametropolitanos”, diz.
<p>O <strong>IDHM das Regiões Metropolitanas</strong> utiliza a mesma metodologia que o IDHM lançado no ano passado e confirma que, embora as disparidades ainda sejam marcantes no Brasil, elas estão diminuindo, tanto entre as próprias RMs – que se encontram todas na categoria de alto desenvolvimento humano – quanto no âmbito intramunicipal. Ainda que as capitais ou municípios-núcleo das RMs possuam melhor IDHM que os conjuntos de municípios do entorno, entre 2000 e 2010, a mediana dos municípios do entorno apresentou maior evolução que a mediana das capitais.</p>	
<p>Ainda assim, é possível encontrar casos extremos em uma mesma Região Metropolitana. No caso de Manaus, por exemplo, a renda média da Unidade de Desenvolvimento Humano (UDH) mais abastada é 47 vezes maior que a da UDH mais carente. Se considerarmos todas as mais de 9.000 UDHs pesquisadas, pode-se notar também uma diferença de 15 anos em termos de expectativa de vida ao nascer. Na Educação, entre as UDHs com melhor desempenho, o percentual de pessoas de 18 ou mais anos de idade com ensino fundamental completo varia de 91% a 96%; já nas UDHs com o pior desempenho, a variação fica entre 21% e 37% .</p>
<p>Jorge Chediek, representante residente do PNUD, chama atenção para que gestores e população utilizem os dados da publicação não apenas para constatar as disparidades, mas para direcionar e reivindicar políticas públicas inclusivas e eficientes para as áreas mais carentes. “Para além de evidenciar o fato de que o país ainda tem um caminho a percorrer na redução das desigualdades em suas cidades, a intenção do Atlas é justamente ajudar no estabelecimento de políticas inclusivas que tenham como fim a melhoria das condições de vida das pessoas”.</p>
<p>“O Atlas é sem dúvida de grande utilidade para gestores, pesquisadores e estudantes. É ainda um instrumento de empoderamento para uma sociedade que está cada dia mais atenta e participante”, reforça Marilena Chaves, presidente da FJP.</p>
<strong>O IDHM das RMs e suas dimensões</strong>
<br/><br/>
<p>O IDHM é composto por três dimensões: longevidade, educação e renda. No caso das regiões metropolitanas, por apresentar o índice mais alto entre as três dimensões, o IDHM Longevidade é o que mais contribui para o resultado final do IDHM nas 16 RMs, ficando o IDHM Renda em segundo lugar. Entretanto, o IDHM Educação foi o que registou os avanços mais expressivos entre 2000 e 2010, sendo  a dimensão que mais avançou em termos absolutos e relativos em todas as RMs. </p>
<p>Em 2000, as maiores diferenças na dimensão da Educação estavam entre as RMs de São Paulo (0,592) e a de Manaus (0,414). Em 2010, as maiores disparidades nesta dimensão ficam entre  São Luís (0,737) e Manaus (0,636).Ou seja,  a diferença de disparidades na dimensão Educação das RMs,entre 2000 e 2010, caiu de 43% para 15,9%. Vale lembrar que a RM de Manaus obteve um crescimento de 53,6% em termos relativos o que ainda assim não foi suficiente para melhorar sua posição em relação às demais RMs. </p>
<p>Na dimensão da Renda, em 2000, a maior diferença estava entre as RMs de São Paulo (0,779) e da Grande São Luís (0,647). Em 2010, os extremos passam a ser RIDE-DF(0,826) e Fortaleza (0,716). Nota-se que entre 2000 e 2010, os crescimentos nos níveis de renda foram expressivos em todas as RMs, mas os maiores avanços foram observados nas RMs de pior performance em 2000, produzindo uma retração relativa na disparidade de renda entre as RMs. A RM de São Luís obteve um crescimento de 58,3% nesta dimensão, enquanto São Paulo, a RM que possuía o melhor desempenho em 2000, cresceu 22,9%.</p>
<p>Na Longevidade,  a maior diferença em 2000 estava entre Porto Alegre (0,809) e São Luís (0,729). Já em 2010, essa diferença maior passa a estar entre as RMs  RIDE-DF (0,857) e São Luís (0,809). Vale também ressaltar que, em 2000, o indicador utilizado para o cálculo do IDHM Longevidade, a esperança de vida ao nascer, mostrava uma diferença de 4,82 anos, que foi reduzido para apenas 2,9 anos, em 2010. Este fato mostra como esta dimensão apresenta índices menos díspares entre as RMs pesquisadas.</p>
