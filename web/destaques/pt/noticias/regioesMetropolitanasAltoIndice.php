<strong> <u> NOTAS PARA EDITORES: </u> </strong>
<br/><br/>
<p>O Atlas do Desenvolvimento Humano no Brasil traz uma ferramenta gratuita de acesso a informações sobre 5.565 municípios brasileiros e agora mais 16 RMs, útil tanto para os gestores públicos quanto para a sociedade em geral. Nele estão contidos o Índice de Desenvolvimento Humano Municipal (IDHM) além de mais de 200 indicadores socioeconômicos do país. Desde o seu lançamento no ano passado, a plataforma do Atlas já teve mais de 5 milhões de acessos.
<p>Apesar de ter sua metodologia baseada no cálculo do IDH Global – publicado anualmente pela sede do PNUD em Nova York para mais de 150 países – , a comparação entre IDHM e IDH não é possível, já que o IDHM é uma adaptação metodológica do IDH ao nível municipal, utilizando outros indicadores em sua composição além de outra base de dados (neste caso, os Censos do IBGE). Assim, ambos agregam as dimensões longevidade, educação e renda, mas com diferentes indicadores e base de dados para retratar estas dimensões. 