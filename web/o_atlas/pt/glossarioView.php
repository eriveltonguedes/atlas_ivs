<div class="block-01-div-subtitle">Descrição e peso dos indicadores que compõem o subíndice IVS Infraestrutura Urbana<br /><br /></div>

<div class="text_met">
    <span><b>Percentual de pessoas em domicílios com abastecimento de água e esgotamento sanitário inadequados</b></span><br />
    Razão entre o número de pessoas que vivem em domicílios cujo abastecimento de água não provém de rede geral e cujo esgotamento sanitário não é realizado por rede coletora de esgoto ou fossa séptica, e a população total residente em domicílios particulares permanentes, multiplicada por cem. São considerados apenas os domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Percentual da população que vive em domicílios urbanos sem serviço de coleta de lixo</b></span><br />
    Razão entre a população que vive em domicílios sem coleta de lixo e a população total residente em domicílios particulares permanentes, multiplicada por cem. Estão incluídas as situações em que a coleta de lixo é realizada diretamente por empresa pública ou privada, ou o lixo é depositado em caçamba, tanque ou depósito fora do domicílio, para posterior coleta pela prestadora do serviço. São considerados apenas os domicílios particulares permanentes, localizados em área urbana
</div>

<div class="text_met">
    <span><b>Percentual de pessoas que vivem em domicílios com renda per capita inferior a meio salário mínimo e que gastam mais de uma hora até o trabalho no total de pessoas ocupadas, vulneráveis e que retornam diariamente do trabalho</b></span><br />
    Razão entre o número de pessoas ocupadas, de 10 anos ou mais de idade, que vivem em domicílios com renda per capita inferior a meio salário mínimo, de agosto de 2010, e que gastam mais de uma hora em deslocamento até o local de trabalho, e o total de pessoas ocupadas nessa faixa etária que vivem em domicílios com renda per capita inferior a meio salário mínimo, de agosto de 2010, e que retornam diariamente do trabalho, multiplicado por cem.
</div>

<div class="block-01-div-subtitle">Descrição e peso dos indicadores que compõem o subíndice IVS Capital Humano<br /><br /></div>

<div class="text_met">
    <span><b>Mortalidade até um ano de idade</b></span><br />
    Número de crianças que não deverão sobreviver ao primeiro ano de vida, em cada mil crianças nascidas vivas
</div>

<div class="text_met">
    <span><b>Percentual de crianças de 0 a 5 anos que não frequentam a escola</b></span><br />
    Razão entre o número de crianças de 0 a 5 anos de idade que não frequentam creche ou escola, e o total de crianças nesta faixa etária (multiplicada por cem).
</div>

<div class="text_met">
    <span><b>Percentual de pessoas de 6 a 14 anos que não frequentam a escola</b></span><br />
    Razão entre o número de pessoas de 6 a 14 anos que não frequentam a escola, e o total de pessoas nesta faixa etária (multiplicada por cem)
</div>

<div class="text_met">
    <span><b>Percentual de mulheres de 10 a 17 anos de idade que tiveram filhos</b></span><br />
    Razão entre o número de mulheres de 10 a 17 anos de idade que tiveram filhos, e o total de mulheres nesta faixa etária (multiplicada por cem)
</div>

<div class="text_met">
    <span><b>Percentual de mães chefes de família, sem fundamental completo e com pelo menos um filho menor de 15 anos de idade, no total de mães chefes de família</b></span><br />
    Razão entre o número de mulheres que são responsáveis pelo domicílio, que não têm o ensino fundamental completo e têm pelo menos um filho de idade inferior a 15 anos morando no domicílio, e o número total de mulheres chefes de família (multiplicada por cem). São considerados apenas os domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Taxa de analfabetismo da população de 15 anos ou mais de idade</b></span><br />
    Razão entre a população de 15 anos ou mais de idade que não sabe ler nem escrever um bilhete simples, e o total de pessoas nesta faixa etária (multiplicada por cem).
</div>

<div class="text_met">
    <span><b>Percentual de crianças que vivem em domicílios em que nenhum dos moradores tem o ensino fundamental completo</b></span><br />
    Razão entre o número de crianças de até 14 anos que vivem em domicílios em que nenhum dos moradores tem o ensino fundamental completo, e a população total nesta faixa etária residente em domicílios particulares permanentes (multiplicada por cem).
</div>

<div class="text_met">
    <span><b>Percentual de pessoas de 15 a 24 anos que não estudam, não trabalham e possuem renda domiciliar per capita igual ou inferior a meio salário mínimo (2010), na população total dessa faixa etária</b></span><br />
    Razão entre as pessoas de 15 a 24 anos que não estudam, não trabalham e com renda per capita inferior a meio salário mínimo, de agosto de 2010, e a população total nesta faixa etária (multiplicada por 100). São considerados apenas os domicílios particulares permanentes.
</div>

<div class="block-01-div-subtitle">Descrição e peso dos indicadores que compõem o subíndice IVS Renda e Trabalho<br /><br /></div>

<div class="text_met">
    <span><b>Proporção de pessoas com renda domiciliar per capita igual ou inferior a meio salário mínimo (2010)</b></span><br />
    Proporção dos indivíduos com renda domiciliar per capita igual ou inferior a R$ 255,00 mensais (em reais de agosto de 2010), equivalente a meio salário mínimo nessa data. O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Taxa de desocupação da população de 18 anos ou mais de idade</b></span><br />
    Percentual da população economicamente ativa (PEA) nessa faixa etária que estava desocupada, ou seja, que não estava ocupada na semana anterior à data do censo, mas havia procurado trabalho ao longo do mês anterior à data dessa pesquisa.
</div>

<div class="text_met">
    <span><b>Percentual de pessoas de 18 anos ou mais sem fundamental completo e em ocupação informal</b></span><br />
    Razão entre as pessoas de 18 anos ou mais sem fundamental completo, em ocupação informal, e a população total nesta faixa etária, multiplicada por cem. Ocupação informal implica que trabalham, mas não são: empregados com carteira de trabalho assinada, militares do exército, da marinha, da aeronáutica, da polícia militar ou do corpo de bombeiros, empregados pelo regime jurídico dos funcionários públicos ou empregadores e trabalhadores por conta própria com contribuição a instituto de previdência oficial.
</div>

<div class="text_met">
    <span><b>Percentual de pessoas em domicílios com renda per capita inferior a meio salário mínimo (de 2010) e dependentes de idosos</b></span><br />
    Razão entre as pessoas que vivem em domicílios com renda per capita inferior a meio salário mínimo, de agosto de 2010, e nos quais a renda de moradores com 65 anos ou mais de idade (idosos) corresponde a mais da metade do total da renda domiciliar, e a população total residente em domicílios particulares permanentes (multiplicada por cem).
</div>

<div class="text_met">
    <span><b>Taxa de atividade das pessoas de 10 a 14 anos de idade</b></span><br />
    Razão das pessoas de 10 a 14 anos de idade que eram economicamente ativas, ou seja, que estavam ocupadas ou desocupadas na semana de referência do censo entre o total de pessoas nesta faixa etária (multiplicada por cem). Considera-se desocupada a pessoa que, não estando ocupada na semana de referência, havia procurado trabalho no mês anterior a essa pesquisa.
</div>

