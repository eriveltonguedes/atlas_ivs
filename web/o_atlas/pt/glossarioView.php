<div class="areatitle">Glossário</div>
<div class="block-01-div-subtitle">DEMOGRAFIA E SAÚDE<br /><br /></div>

<div class="text_met">
  <p>Este bloco contém indicadores de <strong>probabilidade de sobrevivência</strong> (esperança de vida ao nascer, mortalidade infantil, até 5 e até 40 anos)  e de <strong>fecundidade</strong> (fecundidade total), obtidos indiretamente, a partir das informações dos Censos Demográficos e de tábuas de sobrevivência, através da técnica de Brass.<p>
Além disso, este bloco contém indicadores da <strong>estrutura etária</strong> das populações (taxa de envelhecimento e % da população em domicílios com elevada razão de dependência).
</div>

<div class="text_met">
    <span><b>Esperança de vida ao nascer</b></span><br>
    Número médio de anos que as pessoas deverão viver a partir do nascimento, se permanecerem constantes ao longo da vida o nível e o padrão de mortalidade por idade prevalecentes no ano do Censo.    
</div>

<div class="text_met">
    <span><b>Taxa de fecundidade total</b></span><br>
    Número médio de filhos que uma mulher deverá ter ao terminar o período reprodutivo (15 a 49 anos de idade). 
</div>

<div class="text_met">
    <span><b>Mortalidade infantil</b></span><br>
    Número de crianças que não deverão sobreviver ao primeiro ano de vida em cada 1000 crianças nascidas vivas. 
</div>

<div class="text_met">
    <span><b>Mortalidade até 5 anos de idade</b></span><br>
    Probabilidade de morrer entre o nascimento e a idade exata de 5 anos, por 1000 crianças nascidas vivas.
</div>

<div class="text_met">
    <span><b>Probabilidade de sobrevivência até 40 anos</b></span><br>
    Probabilidade de uma criança recém-nascida viver até os 40 anos, se permanecerem constantes ao longo da vida o nível e o padrão de mortalidade por idade do ano do Censo
</div>

<div class="text_met">
    <span><b>% de pessoas que vivem em domicílios com razão de dependência > 75%</b></span><br>
    Razão entre as pessoas que vivem em domicílios nos quais a razão de dependência é maior do que 75% e o total de pessoas residentes em domicílios particulares permanentes multiplicado por 100. Razão de dependência é medida pela razão entre o número de pessoas com 14 anos ou menos e de 65 anos ou mais de idade (população dependente) e o número de pessoas com idade de 15 a 64 anos (população potencialmente ativa) multiplicado por 100.
</div>

<div class="text_met">
    <span><b>Taxa de envelhecimento</b></span><br>
    Razão entre a população de 65 anos ou mais de idade e a população total multiplicado por 100.
</div>


<div class="block-01-div-subtitle">EDUCAÇÃO<br /><br /></div>

<div class="text_met">
    <p>Este bloco contém indicadores relacionados ao <strong>grau de analfabetismo </strong>de diferentes faixas etárias da população (11 a 14 anos, 15 a 17, 15 ou mais, 18 a 24, 18 ou mais, 25 a 29 e 25 ou mais) e ao <strong>grau de escolaridade </strong>(fundamental completo, médio completo e/ou superior completo) da população adulta - 18 anos ou mais e 25 anos ou mais – e de faixas etárias específicas (15 a 17, 18 a 20, 18 a 24).
<p>Além disso, este bloco contém uma série de indicadores de <strong>atendimento ou frequência simples</strong> (frequência a qualquer nível ou modalidade de ensino)  para diferentes faixas etárias ( até 5, 5 a 6, 6 a 14, 15 a 17, 6 a 17, 18 a 24 e 25 a 29) e de <strong>frequência qualificada</strong> (taxas <strong>bruta</strong> e <strong>líquida</strong> de frequência aos níveis fundamental, médio, básico e superior de ensino; % da população com <strong>atraso idade-série</strong> de 2 anos ou mais nos níveis fundamental, médio e básico; <strong>expectativa de anos de estudo</strong> aos 18 anos de idade).
<p>Cabe notar que os indicadores de frequência qualificada foram calculados considerando o ensino regular seriado, mas, no caso das taxas líquida e bruta, foram <strong>também</strong> calculadas considerando, além do ensino regular seriado, o regular não seriado, o EJA (Educação de Jovens e Adultos) e o supletivo. 
</p>
</div>

<div class="text_met">
    <span><b>Expectativa de anos de estudo</b></span><br>
    Número médio de anos de estudo que uma geração de crianças que ingressa na escola deverá completar ao atingir 18 anos de idade, se os padrões atuais se mantiverem ao longo de sua vida escolar.
</div>

<div class="text_met">
    <span><b>Taxa de analfabetismo da população de 11 a 14 anos de idade</b></span><br>
    Razão entre a população de 11 a 14 anos de idade que não sabe ler nem escrever um bilhete simples e o total de pessoas nesta faixa etária multiplicado por 100.
</div>

<div class="text_met">
    <span><b>Percentual da população de 18 anos ou mais com fundamental completo</b></span><br>
    Razão entre as pessoas de 18 anos ou mais que completaram o ensino fundamental em qualquer das modalidades (curso regular seriado e não seriado, Ensino de Jovens e Adultos - EJA - e supletivo) e a população total nessa faixa etária, multiplicado por 100.
</div>

<div class="text_met">
    <span><b>Taxa de atendimento da população de  6 a 14 anos </b></span><br>
    Razão entre a população de 6 a 14 anos de idade que estava frequentando a escola, em qualquer nível ou série, e a população total nesta faixa etária, multiplicada por 100.
</div>

<div class="text_met">
    <span><b>Taxa de frequência bruta ao básico</b></span><br>
    Razão entre o número total de pessoas de qualquer idade frequentando o ensino básico (fundamental ou médio) e a população na faixa etária de 6 a 17 anos multiplicado por 100. Duas taxas foram calculadas, uma considerando apenas a frequência ao ensino básico regular seriado e outra, todas as modalidades (incluindo curso regular seriado e não seriado, Ensino de Jovens e Adultos - EJA - e supletivo).
</div>

<div class="text_met">
    <span><b>Taxa de frequência líquida ao básico</b></span><br>
    Razão entre o número de pessoas na faixa etária de 6 a 17 anos frequentando o ensino básico (fundamental ou médio) e a população total dessa mesma faixa etária multiplicado por 100. Duas taxas foram calculadas, uma considerando apenas a frequência ao ensino básico regular seriado e outra, todas as modalidades (incluindo curso regular seriado e não seriado, Ensino de Jovens e Adultos - EJA - e supletivo).
</div>

<div class="text_met">
    <span><b>Percentual da população de 6 a 17 anos de idade frequentando o ensino básico que tem 2 anos ou mais de atraso idade-série</b></span><br>
    Razão entre o número de pessoas de 6 a 17 anos frequentando o ensino básico regular seriado (fundamental + médio) com atraso idade-série de 2 anos ou mais e o número total de pessoas nessa faixa etária frequentando esse nível de ensino multiplicado por 100. O atraso idade-série é calculado pela fórmula: [(idade - 5) - número da série frequentada], na qual o número da série frequentada é 10, 11 e 12 para as pessoas que frequentam a 1ª, a 2ª e a 3ª séries do ensino médio, respectivamente. As pessoas de 6 a 14 anos frequentando a pré-escola foram consideradas como se estivessem no 1º ano do ensino fundamental. As pessoas de 15 a 17 anos frequentando a 4ª série do ensino médio foram consideradas como já tendo concluído esse nível de ensino.
</div>

<div class="block-01-div-subtitle">RENDA<br /><br /></div>

<div class="text_met">
    Este bloco contém indicadores de <strong>nível de renda </strong>(renda <i><i> per capita </i></i>, total e por quintos da população, valor dos quintis); de <strong>pobreza</strong>, de acordo com as linhas de pobreza de R$70,00, R$140,00 e R$255,00 de renda domiciliar <i><i> per capita </i></i> (proporções de pessoas extremamente pobres, de pobres e de vulneráveis à pobreza e proporções de crianças extremamente pobres, pobres e vulneráveis à pobreza); e de <strong>desigualdade de renda </strong>(índices de Gini e Theil-L, razão entre a renda <i><i> per capita </i></i> dos 20% mais ricos e dos 40% mais pobres).
</div>

<div class="text_met">
    <span><b>Renda <i><i> per capita </i></i></b></span><br>
    Razão entre o somatório da renda de todos os indivíduos residentes em domicílios particulares permanentes e o número total desses indivíduos. Valores em reais de 01/agosto de 2010.
</div>

<div class="text_met">
    <span><b>Proporção de extremamente pobres</b></span><br>
    Proporção dos indivíduos com renda domiciliar <i><i> per capita </i></i> igual ou inferior a R$ 70,00 mensais, em reais de agosto de 2010. O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Proporção de pobres</b></span><br>
    Proporção dos indivíduos com renda domiciliar <i><i> per capita </i></i> igual ou inferior a R$ 140,00 mensais, em reais de agosto de 2010. O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Proporção de vulneráveis à pobreza</b></span><br>
    Proporção dos indivíduos com renda domiciliar <i><i> per capita </i></i> igual ou inferior a R$ 255,00 mensais, em reais de agosto de 2010, equivalente a 1/2 salário mínimo nessa data. O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Razão 20% mais ricos / 40% mais pobres</b></span><br>
    Medida do grau de desigualdade existente na distribuição de indivíduos segundo a renda domiciliar <i><i> per capita </i></i>. Compara a renda <i><i> per capita </i></i> média dos indivíduos pertencentes ao quinto mais rico dessa distribuição com a renda <i> per capita </i> média dos indivíduos pertencentes aos dois quintos mais pobres. O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>Índice de Theil - L</b></span><br>
    Mede a desigualdade na distribuição de indivíduos segundo a renda domiciliar <i> per capita </i>, excluídos aqueles com renda domiciliar <i> per capita </i> nula. É o logaritmo da razão entre as médias aritmética e geométrica da renda domiciliar <i> per capita </i> dos indivíduos, sendo nulo quando não existir desigualdade de renda entre eles e tendente ao infinito quando a desigualdade tender ao máximo.
</div>


<div class="text_met">
    <span><b>Índice de Gini</b></span><br>
    Mede o grau de desigualdade existente na distribuição de indivíduos segundo a renda domiciliar <i> per capita </i>. Seu valor varia de 0, quando não há desigualdade (a renda domiciliar <i> per capita </i> de todos os indivíduos tem o mesmo valor), a 1, quando a desigualdade é máxima (apenas um indivíduo detém toda a renda).O universo de indivíduos é limitado àqueles que vivem em domicílios particulares permanentes.
</div>

<div class="block-01-div-subtitle">TRABALHO<br /><br /></div>

<div class="text_met">
   <p> Este bloco contém <strong>indicadores de atividade e desocupação</strong> (taxa de atividade e taxa de desocupação) para diferentes faixas etárias da população (10 ou mais, 10 a 14, 15 a 17, 18 a 24 e 25 a 29).
<p>Além disso, este bloco contém uma série de <strong>indicadores de caracterização dos ocupados</strong> de 18 anos ou mais de idade, abrangendo <strong>setor </strong>de atividade, grau de <strong>formalização</strong>, grau de <strong>escolaridade</strong>, <strong>posição na ocupação </strong>e <strong>rendimento</strong> do trabalho.
</p>
</div>

<div class="text_met">
    <span><b>Taxa de atividade - 10 anos ou mais</b></span><br>
    Razão entre as pessoas de 10 anos ou mais de idade que eram economicamente ativas, ou seja, que estavam ocupadas ou desocupadas na semana de referência do Censo e o total de pessoas nesta faixa etária multiplicado por 100. Considera-se  desocupada a pessoa que, não estando ocupada na semana de referência, havia procurado trabalho no mês anterior a essa pesquisa.
</div>

<div class="text_met">
    <span><b>Taxa de desocupação - 10 anos ou mais</b></span><br>
    Corresponde ao percentual da população economicamente ativa (PEA) nessa faixa etária que estava desocupada, ou seja, que não estava ocupada na semana anterior à data do Censo, mas havia procurado trabalho ao longo do mês anterior à data dessa pesquisa. A PEA é o somatório das pessoas ocupadas e desocupadas.
</div>

<div class="text_met">
    <span><b>Grau de formalização dos ocupados - 18 anos ou mais</b></span><br>
    Razão entre o número de pessoas de 18 anos ou mais formalmente ocupadas e o número total de pessoas ocupadas nessa faixa etária multiplicado por 100. Foram considerados como formalmente ocupados os empregados com carteira de trabalho assinada, os militares do exército, da marinha, da aeronáutica, da polícia militar ou do corpo de bombeiros, os empregados pelo regime jurídico dos funcionários públicos, assim como os empregadores e trabalhadores por conta própria que eram contribuintes de instituto de previdência oficial.
</div>

<div class="text_met">
    <span><b>Percentual dos ocupados no setor serviços</b></span><br>
    Razão entre o número de pessoas de 18 anos ou mais de idade ocupadas no setor de serviços e o número total de pessoas ocupadas nessa faixa etária, multiplicada por 100.
</div>

<div class="text_met">
    <span><b>Percentual dos ocupados com fundamental completo</b></span><br>
    Razão entre o número de pessoas de 18 anos ou mais de idade ocupadas que já concluíram o ensino fundamental (regular seriado, regular não seriado, EJA ou supletivo) e o número total de pessoas ocupadas nessa faixa etária, multiplicada por 100.
</div>

<div class="text_met">
    <span><b>Percentual de ocupados de 18 anos ou mais que são trabalhadores por conta própria</b></span><br>
    Razão entre o número de trabalhadores por conta própria de 18 anos ou mais de idade e o número total de pessoas ocupadas nessa faixa etária, multiplicada por 100.
</div>

<div class="text_met">
    <span><b>Rendimento médio dos ocupados</b></span><br>
    Média dos rendimentos de todos os trabalhos das pessoas ocupadas de 18 anos ou mais de idade. Valores em reais de agosto de 2010.
</div>

<div class="block-01-div-subtitle">HABITAÇÃO<br /><br /></div>

<div class="text_met">
    Este bloco contém indicadores relacionados às condições habitacionais, envolvendo <strong>características da habitação</strong> (tipo de parede e número de pessoas por dormitório) e <strong>acesso a serviços</strong> (água, esgoto, lixo e luz).
</div>

<div class="text_met">
    <span><b>Percentual da população que vive em domicílios com água encanada</b></span><br>
    Razão entre a população que vive em domicílios particulares permanentes com água canalizada para um ou mais cômodos e a população total residente em domicílios particulares permanentes multiplicado por 100. A água pode ser proveniente de rede geral, de poço, de nascente ou de reservatório abastecido por água das chuvas ou carro-pipa.
</div>

<div class="text_met">
    <span><b>Percentual da população que vive em domicílios com banheiro e água encanada</b></span><br>
    Razão entre a população que vive em domicílios particulares permanentes com água encanada em pelo menos um de seus cômodos e com banheiro exclusivo e a população total residente em domicílios particulares permanentes multiplicado por 100. A água pode ser proveniente de rede geral, de poço, de nascente ou de reservatório abastecido por água das chuvas ou carro-pipa. Banheiro exclusivo é definido como cômodo que dispõe de chuveiro ou banheira e aparelho sanitário.
</div>

<div class="text_met">
    <span><b>% da população em domicílios com densidade > 2</b></span><br>
    Percentual da população que vive em domicílios com densidade superior a 2 pessoas por dormitório.
    Razão entre a população que vive em domicílios particulares permanentes com densidade superior a 2 e a população total residente em domicílios particulares permanentes multiplicado por 100. A densidade do domicílio é dada pela razão entre o total de moradores do domicílio e o número total de cômodos usados como dormitório.
</div>

<div class="text_met">
    <span><b>Percentual da população que vive em domicílios urbanos com serviço de coleta de lixo</b></span><br>
    Razão entre a população que vive em domicílios com coleta de lixo e a população total residente em domicílios particulares permanentes multiplicado por 100. Estão incluídas as situações em que a coleta de lixo realizada diretamente por empresa pública ou privada, ou o lixo é depositado em caçamba, tanque ou depósito fora do domicílio, para posterior coleta pela prestadora do serviço. São considerados apenas os domicílios particulares permanentes localizados em área urbana.
</div>

<div class="text_met">
    <span><b>Percentual da população que vive em domicílios com energia elétrica</b></span><br>
    Razão entre a população que vive em domicílios particulares permanentes com iluminação elétrica e a população total residente em domicílios particulares permanentes multiplicado por 100. Considera-se iluminação proveniente ou não de uma rede geral, com ou sem medidor.
</div>

<div class="text_met">
    <span><b>% de pessoas em domicílios com abastecimento de água e esgotamento sanitário inadequados</b></span><br>
    Razão entre as pessoas que vivem em domicílios cujo abastecimento de água não provem de rede geral e cujo esgotamento sanitário não é realizado por rede coletora de esgoto ou fossa séptica e a população total residente em domicílios particulares permanentes multiplicado por 100. São considerados apenas os domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>% de pessoas em domicílios com paredes inadequadas</b></span><br>
    Razão entre as pessoas que vivem em domicílios cujas paredes não são de alvenaria nem de madeira aparelhada e a população total residente em domicílios particulares permanentes multiplicado por 100. São considerados apenas os domicílios particulares permanentes.
</div>

<div class="block-01-div-subtitle">VULNERABILIDADE<br /><br /></div>

<div class="text_met">
    Este bloco reúne indicadores de <strong>vulnerabilidade por dimensão</strong>, selecionados dos demais blocos de indicadores, e de <strong>vulnerabilidade multidimensional</strong>, que conjugam vulnerabilidades simultâneas em diferentes dimensões. 
</div>
