    Razão entre as pessoas que vivem em domicílios vulneráveis à pobreza (com renda <i> per capita </i> inferior a 1/2 salário mínimo de agosto de 2010) e nos quais pelo menos metade da renda domiciliar provém de moradores com 65 anos ou mais de idade e a população total que reside em domicílios vulneráveis à pobreza e com pelo menos uma pessoa idosa multiplicado por 100. São considerados apenas os domicílios particulares permanentes. 
</div>

<div class="text_met">
    <span><b>% de pessoas de 15 a 24 anos que não estudam nem trabalham e são vulneráveis à pobreza </b></span><br>
    Razão entre as pessoas de 15 a 24 anos que não estudam, não trabalham e são vulneráveis à pobreza e a população vulnerável à pobreza nessa mesma faixa etária (15 a 24 anos), multiplicado por 100. Define-se como vulnerável à pobreza a pessoa que mora em domicílio com renda <i> per capita </i> inferior a 1/2 salário mínimo de agosto de 2010. São considerados apenas os domicílios particulares permanentes. 
</div>

<div class="text_met">
    <span><b>% de mães chefes de família sem fundamental completo e com filhos menores de 15 anos</b></span><br>
    Razão entre o número de mulheres que são responsáveis pelo domicílio, não têm o ensino fundamental completo e têm pelo menos 1 filho de idade inferior a 15 anos morando no domicílio e o número total de mulheres chefes de família com filho menor de 15 anos de idade, multiplicado por 100. São considerados apenas os domicílios particulares permanentes.
</div>

<div class="text_met">
    <span><b>% de pessoas vulneráveis à pobreza e que gastam mais de uma hora até o trabalho</b></span><br>
    Razão entre as pessoas ocupadas de 10 anos ou mais de idade que vivem em domicílios vulneráveis à pobreza (com renda <i> per capita </i> inferior a 1/2 salário mínimo de agosto de 2010) e que gastam mais de uma hora em deslocamento até o local de trabalho e o total de pessoas ocupadas nessa faixa etária que vivem em domicílios vulneráveis à pobreza e que retornam diariamente do trabalho, multiplicado por 100. 
</div>

<div class="block-01-div-subtitle">POPULAÇÃO<br /><br /></div>

<div class="text_met">
    Este bloco traz as populações de homens e de mulheres <strong>por faixas etárias de 5 anos</strong> (0 a 4, 5 a 9, 10 a 14, etc), além de<strong> populações específicas </strong>utilizadas como referência nos indicadores.  
</div>

<div class="text_met">
    <span><b>População feminina de 25 a 29 anos de idade</b></span><br>
    População nesta faixa etária e do sexo feminino.
</div>

<div class="text_met">
    <span><b>População total em domicílios particulares permanentes</b></span><br>
    População residente em domicílios particulares permanentes. Exclui os residentes em domicílios coletivos, como pensões, hotéis, prisões, quartéis, hospitais.
</div>

<div class="text_met">
    <span><b>PEA - 10 anos ou mais</b></span><br>
    População economicamente ativa. Corresponde ao número de pessoas nessa faixa etária que, na semana de referência do Censo, encontravam-se ocupadas no mercado de trabalho ou que, encontrando-se desocupadas, tinham procurado trabalho no mês anterior à data da pesquisa.
</div>

<div class="block-01-div-subtitle">DESENVOLVIMENTO HUMANO<br /><br /></div>

<div class="text_met">
    Este bloco inclui o <strong>Índice de Desenvolvimento Humano</strong>, os subíndices que  o compõem, relativos às dimensões <strong>Renda</strong>, <strong>Educação</strong> e <strong>Longevidade</strong>, além dos subíndices que compõem o índice da dimensão Educação (os índices das dimensões Renda e Longevidade são construídos a partir de um único indicador - renda per capita e esperança de vida ao nascer, respectivamente).
</div>

<div class="text_met">
    <span><b>IDHM -	Índice de Desenvolvimento Humano Municipal</b></span><br>
    Índice de Desenvolvimento Humano Municipal. Média geométrica dos índices das dimensões Renda, Educação e Longevidade, com pesos iguais.
</div>

<div class="text_met">
    <span><b>IDHM Renda (Índice de Desenvolvimento Humano Municipal - Dimensão Renda)</b></span><br>
    Índice da dimensão Renda que é um dos 3 componentes do IDHM. É obtido a partir do indicador Renda <i> per capita </i>, através da fórmula: [ln (valor observado do indicador) - ln (valor mínimo)] / [ln (valor máximo) - ln (valor mínimo)], onde os valores mínimo e máximo são R$ 8,00 e R$ 4.033,00 (a preços de agosto de 2010).
</div>

<div class="text_met">
    <span><b>IDHM Longevidade (Índice de Desenvolvimento Humano Municipal  - Dimensão Longevidade)</b></span><br>
    Índice da dimensão Longevidade que é um dos 3 componentes do IDHM. É obtido a partir do indicador Esperança de vida ao nascer, através da fórmula: [(valor observado do indicador) - (valor mínimo)] / [(valor máximo) - (valor mínimo)], onde os valores mínimo e máximo são 25 e 85 anos, respectivamente.
</div>

<div class="text_met">
    <span><b>IDHM Educação (Índice de Desenvolvimento Humano Municipal - Dimensão Educação)</b></span><br>
    Índice sintético da dimensão Educação que é um dos 3 componentes do IDHM. É obtido através da média geométrica do subíndice de frequência de crianças e jovens à escola, com peso de 2/3, e do subíndice de escolaridade da população adulta, com peso de 1/3.
</div>

<div class="text_met">
    <span><b>Subíndice de escolaridade fundamental da população adulta - IDHM Educação</b></span><br>
    Subíndice selecionado para compor o IDHMEducação, representando o nível de escolaridade da população adulta. É obtido pelo indicador % de jovens e adultos com 18 anos ou mais com o fundamental completo.
</div>

<div class="text_met">
    <span><b>Subíndice de frequência escolar da população jovem - IDHM Educação</b></span><br>
    Subíndice selecionado para compor o IDHMEducação, representando a frequência de crianças e jovens à escola em  séries adequadas à sua idade.  É obtido através da média aritmética simples de 4  indicadores:  % de crianças de 5 a 6 anos na escola, % de  crianças de 11 a 13 anos no 2º ciclo do fundamental, % de jovens de 15 a 17 anos com o fundamental completo e % de jovens de 18 a 20 anos com o médio completo.
</div>

