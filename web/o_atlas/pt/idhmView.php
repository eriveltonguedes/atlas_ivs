        <div id="text_calculo_idhm">
            <p>A construção da metodologia de cálculo do IDHM teve como objetivo adequar a metodologia do IDH Global para:</p><br />
            <div id="a_b" style='margin-right: 30px;'>
                <b><font color="#BDBDBD" size="5">a)</font></b><br />
                Ajustar a metodologia ao contexto brasileiro, buscando indicadores mais adequados para avaliar as condições de núcleos sociais menores – os municípios.
            </div>
            <div id="a_b">
                <b><font color="#BDBDBD" size="5">b)</font></b><br />
                Adaptar a metodologia do IDH Global aos indicadores disponíveis nos Censos Demográficos brasileiros, de forma a garantir mesma fonte de dados e comparabilidade entre todos os municípios.
            </div>
            <div class="clear"></div>
            
            <br />
            <p><b>Vida longa e saudável</b> é medida pela expectativa de vida ao nascer, calculada por método indireto, a partir dos dados dos Censos Demográficos do IBGE. Esse indicador mostra o número médio de 
                anos que uma pessoa nascida em determinado município viveria a partir do nascimento, mantidos os mesmos padrões de mortalidade. 
            </p>
            
            <br />
            <p><b>Acesso a conhecimento </b>é medido por meio de dois indicadores. A escolaridade da população adulta é medida pelo percentual de pessoas de 18 anos ou mais de idade com ensino fundamental completo - tem peso 1. O fluxo escolar da população jovem é medido pela média aritmética do percentual de crianças de 5 a 6 anos frequentando a escola, do percentual de jovens de 11 a 13 anos frequentando os anos finais do ensino fundamental, do percentual de jovens de 15 a 17 anos com ensino fundamental completo e do percentual de jovens de 18 a 20 anos com ensino médio completo - tem peso 2. A medida acompanha a população em idade escolar em quatro momentos importantes da sua formação. Isso facilita aos gestores identificar se crianças e jovens estão nas séries adequadas nas idades certas. A média geométrica desses dois componentes resulta no IDHM Educação. Os dados são do Censo Demográfico do IBGE. 
            </p>
            
            <br />
            <p><b>Padrão de vida </b> é medido pela renda municipal <i>per capita</i>, ou seja, a renda média dos residentes de determinado município. É a soma da renda de todos os 
                residentes, dividida pelo número de pessoas que moram no município – inclusive crianças e pessoas sem registro de renda. Os dados são dos Censos Demográficos do IBGE.</p>
