    O IDHM é uma adaptação metodológica do IDH ao nível municipal. Ambos os índices agregam as dimensões saúde, educação e renda, mas alguns dos indicadores usados para retratar estas dimensões diferem.
</div>

<div class="text_met">
    Assim como no IDH global, o <b>IDHM Educação</b> é uma composição de dois indicadores: um indicador fornece informação sobre a situação educacional da <b>população adulta</b> e um referente à <b>população em idade escolar</b> (jovens). Entretanto, as variáveis são outras. No caso da população adulta, a média de anos de estudo de pessoas de 25 anos ou mais, tal como é medido no IDH Global, não pode ser obtida das informações do Censo 2010 e foi substituída pela <b>proporção da população adulta de 18 anos ou mais que concluiu o ensino fundamental</b>. Este indicador permite uma boa avaliação do nível de carência da população adulta em relação à escolaridade considerada básica (nível fundamental). No caso da população jovem, a metodologia aplicada pelo IDH Global a partir de 2010 – a expectativa de vida escolar – é uma medida de retenção das pessoas na escola, independentemente da repetência, e inclui o ensino superior. A adaptação do IDHM para os contextos nacional e municipal foi feita com uma combinação de 4 indicadores que permitem verificar até que ponto as crianças e os jovens estão frequentando e completando determinados ciclos da escola. O subíndice resultante, o <b>fluxo escolar da população jovem</b>, é a média aritmética do percentual de crianças de 5 a 6 anos frequentando a escola, do percentual de jovens de 11 a 13 anos frequentando os anos finais do ensino fundamental (6º ao 9º ano), do percentual de jovens de 15 a 17 anos com ensino fundamental completo e do percentual de jovens de 18 a 20 anos com ensino médio completo.		
</div>

<div class="text_met">
    Enquanto o IDH Global calcula o componente renda pela Renda Nacional Bruta per capita, em poder de paridade de compra (ppp, Banco Mundial 2005), o <b>IDHM Renda</b> considera a renda municipal per capita, ou seja, a renda média mensal dos indivíduos residentes em determinado município, expressa em Reais por meio da renda per capita municipal.
</div>

<div class="text_met">
    Assim como o IDH Global, o <b>IDHM Longevidade</b> é calculado pela esperança de vida ao nascer, ou seja, o número médio de anos que as pessoas viveriam a partir do nascimento, mantidos os mesmos padrões de mortalidade observados no ano de referência.
</div>

<div class="text_met">
    Quando comparamos ambos os índices, um fator importante a ser destacado é a fonte de dados. Para o cálculo do IDHM, todos os dados foram extraídos dos Censos Demográficos do IBGE, ao passo que o IDH Global traz dados do Departamento de Assuntos Econômicos e Sociais da ONU, Instituto de Estatísticas da UNESCO, Banco Mundial e Fundo Monetário Internacional. A opção por restringir as informações municipais a uma única fonte garante a maior comparabilidade entre localidades do país.
</div>

<div class="text_met" id="6">
    <span><b>Por que adaptar o IDH Global ao nível municipal?</b></span><br>
    O Brasil foi um dos pioneiros em adaptar o IDH ao nível municipal, em 1998. Desde então, diversos países vêm formulando seus próprios índices municipais, adaptando a metodologia do IDH Global às necessidades locais. São exemplos os casos da Gâmbia, que incorporou diferentes variáveis (a taxa de alfabetização adulta e a expectativa de vida escolar) para contabilizar a evasão escolar e medir a dimensão educação, e da China, que criou o Índice de Risco à Saúde, incluindo variáveis como a exposição à poluição do ar e da água, aspectos nutricionais e capacidade de atendimento dos serviços de saúde. É importante ressaltar que, ao adaptar o IDH ao nível nacional, os indicadores podem variar. De acordo com as diretrizes dos relatórios globais sobre desenvolvimento humano do PNUD, a escolha dos indicadores utilizados deve ser baseada na disponibilidade de dados subnacionais e na contextualização com as realidades locais. Ao manter as três dimensões, garante-se que o índice criado mantenha a padronização do IDH, e as adequações metodológicas permitem um maior refinamento do índice municipal.
</div>

<div class="text_met" id="7">
    <span><b>Posso comparar o IDH Global com o IDHM?</b></span><br>
    Não. O IDH e o IDHM são, ambos, índices sintéticos que compreendem indicadores de três dimensões do desenvolvimento humano: saúde/longevidade, educação e renda. Entretanto, os indicadores escolhidos para esta composição são diferentes entre o IDH e o IDHM, assim como as fontes de dados. Os objetivos dos dois índices são diferentes: o IDH serve para medir o desenvolvimento humano de países em contexto global, ou seja, em relação a si mesmos, porém inseridos em uma dinâmica pontilhada por outros países. Já o IDHM serve para comparar territórios nacionais entre si, como UFs, municípios, regiões metropolitanas e UDHs.
</div>

<div class="text_met" id="8">
    <span><b>Por que o IDH Global do Brasil, publicado anualmente pelo PNUD, é diferente do IDHM do Brasil no Atlas?</b></span><br>
    Os objetivos dos dois índices são diferentes: o IDH serve para medir o desempenho de países, e o IDHM para acompanhar o desempenho das UDs, municípios, regiões metropolitanas e UDHs brasileiras. Ainda que estejam medindo semelhantes fenômenos – longevidade, acesso à educação formal, e renda –, como os indicadores e as fontes de dados são diferentes, eles não podem ser comparados. O cálculo do IDHM do Brasil serve apenas para avaliar o desempenho dos municípios, regiões metropolitanas e UDHs em comparação consigo mesmos, com as médias nacionais e estaduais, e <b>não pode ser comparado com o IDH de outros países</b>. Para a comparação entre países é preciso utilizar o IDH do Brasil publicado anualmente pela sede do PNUD em Nova York.
</div>

<div class="text_met" id="9">
    <span><b>Por que os valores dos indicadores que compõem o IDHM do Brasil são diferentes dos publicados pelo IDH do Brasil pelo PNUD?</b></span><br>
    Para calcular a esperança de vida ao nascer em nível municipal, o ponto de partida são as informações dos censos, mas trata-se de um cálculo indireto, utilizando uma adaptação da metodologia de Brass. Para o país, utiliza-se a informação do IBGE. O IDH Global usa fontes internacionais que incluem projeções e nem sempre estão com o mesmo nível de atualização.
</div>

<div class="text_met">
    No caso da renda, a adotada no IDH Global é a renda nacional bruta <i>per capita</i>, que é fruto de um cálculo indireto, obtido no âmbito das contas nacionais, que também geram indicadores como o Produto Interno Bruto e outros indicadores macroeconômicos. Já a adotada no IDHM é a renda das pessoas, calculada a partir dos quesitos sobre rendimentos dos censos demográficos. Portanto, são conceitos e fontes diferenciados.
    No caso da educação, todas as variáveis que compõem o IDHM são diferentes das variáveis que compõem o IDH, assim, os dados são diferentes.
</div>

<div class="block-01-div-subtitle">A metodologia de cálculo do IDHM<br /><br /></div>

<div class="text_met" id="10">
    <span><b>Como é calculado o IDHM?</b></span><br>
    O IDHM é um índice composto que agrega 3 das mais importantes dimensões do desenvolvimento humano: a oportunidade de viver uma vida longa e saudável, de ter acesso a conhecimento e ter um padrão de vida que garanta as necessidades básicas, representadas pela saúde, educação e renda.
</div>

<div class="text_met">
    <b>Vida longa e saudável</b> é medida pela expectativa de vida ao nascer, calculada por método indireto a partir dos dados dos Censos Demográficos do IBGE. Esse indicador mostra o número médio de anos que as pessoas viveriam a partir do nascimento, mantidos os mesmos padrões de mortalidade observados no ano de referência.		
</div>

<div class="text_met">
    <b>Acesso a conhecimento</b> é medido pela composição de indicadores de escolaridade da população adulta e do fluxo escolar da população jovem. A escolaridade da população adulta é medida pelo percentual de pessoas de 18 anos ou mais de idade com fundamental completo; e tem peso 1. O fluxo escolar da população jovem é medido pela média aritmética do percentual de crianças entre 5 e 6 anos frequentando a escola, do percentual de jovens entre 11 e 13 anos frequentando os anos finais do ensino fundamental (6º a 9º ano), do percentual de jovens entre 15 e 17 anos com ensino fundamental completo e do percentual de jovens entre 18 e 20 anos com ensino médio completo; e tem peso 2. A medida acompanha a população em idade escolar em quatro momentos importantes da sua formação. A média geométrica desses dois componentes resulta no IDHM Educação. Os dados são do Censo Demográfico do IBGE.
</div>

<div class="text_met">
    <b>Padrão de vida</b> é medido pela renda municipal per capita, ou seja, a renda média de cada residente de determinado município. É a soma da renda de todos os residentes, dividida pelo número de pessoas que moram no município – inclusive crianças e pessoas sem registro de renda. Os dados são do Censo Demográfico do IBGE. 
</div>

<div class="text_met">
    Os três componentes acima são agrupados por meio da média geométrica, resultando no IDHM.
</div>

<div class="text_met">
    Um detalhamento do cálculo do IDHM está disponível na seção <a href="<?php echo $path_dir . $_SESSION['lang'] . '/o_atlas/metodologia/idhm_longevidade'; ?>">"Metodologia"</a>.
</div>

<div class="text_met" id="11">
    <span><b>As três dimensões do IDHM têm o mesmo peso?</b></span><br>
    Sim. O IDHM atribui pesos iguais às três dimensões. O IDHM de cada município é fruto da média geométrica desses três subíndices. A adoção de pesos iguais é baseada na concepção de que as três dimensões são igualmente importantes e não são substituíveis. 
</div>

<div class="text_met" id="12">
    <span><b>A metodologia de cálculo do IDHM sempre foi a mesma? Por que houve mudanças nos indicadores que o compõem?</b></span><br>
    O IDHM continua a ser um índice composto que mede o desenvolvimento em três dimensões básicas – saúde, educação e renda. Na metodologia adotada em 1998 e em 2003, o IDHM media a educação por dois indicadores com pesos diferentes: taxa de alfabetização de pessoas acima de 15 anos de idade (com peso dois) e a taxa bruta de frequência à escola (com peso um); a saúde era medida pela a esperança de vida ao nascer; e a renda pela renda municipal per capita. A nova metodologia do IDHM busca acompanhar as modificações metodológicas ocorridas no IDH Global em 2010, ao mesmo tempo em que é adaptado metodológica e conceitualmente a fim de melhor retratar o contexto brasileiro, utilizando os Censos Demográficos do Instituto Brasileiro de Geografia e Estatística (IBGE). Mais informações sobre o cálculo do IDHM estão disponíveis na seção <a href="<?php echo $path_dir . $_SESSION['lang'] . '/o_atlas/metodologia/idhm_longevidade'; ?>">"Metodologia"</a>.
</div>

<div class="text_met" id="13">
    <span><b>Por que foi adotada a média geométrica na agregação dos componentes do IDHM?</b></span><br>
    Anteriormente, o IDHM agregava suas 3 dimensões por meio de uma média aritmética simples: o IDHM de um município era o resultado da soma de seus subíndices, dividido por três (IDHM Educação + IDHM Longevidade + IDHM Renda / 3). Assim, o fraco desempenho em uma dimensão poderia ser compensado por um desempenho melhor em outra. Por exemplo, uma localidade que apresentava baixa esperança de vida, mas alta renda municipal, poderiam ter IDHM semelhante às localidades que estavam com equilíbrio no desempenho concomitantes entre as três dimensões.
</div>

<div class="text_met">
    No IDHM do Atlas Brasil, foi adotada a média geométrica: as dimensões são multiplicadas e o produto é extraído pela raiz cúbica (<img src="<?php echo './assets/img/oAtlas/' . $_SESSION['lang'] . '/formula.gif' ?>">). Deste modo, a média geométrica reduz o nível de substituição entre as dimensões. Ou seja, um baixo desempenho em uma dimensão não é mais linearmente compensado pelo elevado desempenho em outra. Assim, o IDHM reflete desempenhos nas três dimensões. O desempenho das localidades, tanto na renda, quanto na saúde e na educação, deve ser harmonioso. 
</div>

<div class="text_met" id="14">
    <span><b>Posso comparar o IDHM de 1991 e o de 2000, publicados em 2003, com o IDHM 2010, publicado em 2013?</b></span><br>
    Não. Apesar de avaliar as mesmas dimensões, como saúde, educação e renda, a metodologia utilizada para obter as variáveis foi modificada na nova edição do IDHM, o que impede uma comparação adequada dos dados. Para fazer comparações e análises das décadas passadas, o Atlas Brasil traz, além dos dados de 2010, os dados de 2000 e 1991 recalculados de acordo com os ajustes metodológicos, e considera também as divisões municipais ocorridas entre 2000 (5.507 municípios) e 2010 (5.565 municípios).
</div>

<div class="text_met" id="15">
    <span><b>Por que a posição ocupada por meu município em 1991 e/ou 2000 mudou neste Novo Atlas em relação à publicada anteriormente?</b></span><br>
    Não se trata de deduzir que a posição de seu município nos anos anteriores estava errada, a questão é que esta posição foi agora revista à luz dos desafios colocados atualmente para o desenvolvimento humano, que o IDHM procura retratar.
</div>

<div class="text_met">
    Apesar de avaliar as mesmas dimensões, saúde, educação e renda, a metodologia utilizada para obter os índices do IDHM nesta edição não é a mesma do Atlas anterior, acompanhando a evolução metodológica necessária aos novos desafios, como aconteceu com o IDH de países, divulgado anualmente pela sede do PNUD em Nova York.
</div>

<div class="text_met">
    O IDH muda para explicitar melhor os novos desafios colocados em cada período. E assim muda também a adaptação que fazemos para este índice em nível municipal, o IDHM. Por exemplo, o IDHM Educação até 2000 privilegiou o fato de as crianças e jovens estarem frequentando a escola, agora o desafio é estar na escola e na série adequada à idade, e também a garantia do acesso de crianças de 5 e 6 anos ao sistema de ensino.
</div>

<div class="text_met">
    Este novo Atlas traz, além dos dados de 2010, os dados de 2000 e 1991 recalculados de acordo com os ajustes metodológicos realizados: nos indicadores e nas fórmulas de cálculo. Além disso, o IDHM leva em conta a criação de novos municípios devido às divisões municipais ocorridas entre 2000 (5.507 municípios) e 2010 (5.565 municípios) e reproduz o espaço atual dos novos municípios no passado, embora este espaço não fosse um município e, sim, parte de um ou mais municípios. Desta forma, é mudado também o espaço daqueles municípios que perderam território. Ele é visto face à configuração municipal do presente e não do passado.
</div>

<div class="text_met">
    Todo este esforço – tanto a aplicação da nova metodologia nos anos anteriores quanto a compatibilização das áreas municipais considerando a criação de novos municípios – é realizado para permitir uma comparação temporal e espacial dos indicadores do Atlas.
</div>

<div class="text_met">
    Vale ressaltar que a comparação propiciada pelas técnicas empregadas não é trivial,  pois é difícil contextualizar 10 ou 20 anos atrás com as demandas de hoje. A comparação tem o sentido de permitir acompanhar a evolução dos municípios, sob a ótica das demandas e contextos atuais.
</div>

<div class="text_met" id="16">
    <span><b>A renda per capita pode ser usada como proxy para mensurar o desenvolvimento humano?</b></span><br>
    Não. A renda per capita nos permite analisar o desenvolvimento do ponto de vista puramente econômico e, ainda que a renda seja uma variável importante para o alcance de um padrão de vida digno, ela não garante automaticamente o desenvolvimento humano. O conceito de desenvolvimento humano é amplo e parte do pressuposto de que, para avaliar o avanço na qualidade de vida de uma população, é preciso ir além do viés puramente econômico e considerar outras características sociais, culturais e políticas que influenciam a qualidade da vida humana. A renda é importante, mas como um dos meios do desenvolvimento e não como seu fim. É uma mudança de perspectiva: com o desenvolvimento humano, o foco é transferido do crescimento econômico, ou da renda, para o ser humano. 
</div>

<div class="text_met" id="17">
    <span><b>Por que o IDHM não inclui dimensões como participação social, gênero e equidade?</b></span><br>
    O IDHM é calculado a partir dos dados do Censo Demográfico do IBGE e fornece, em um único número, informações sobre algumas questões-chave do desenvolvimento humano – saúde, educação e renda. Um quadro mais completo sobre o desenvolvimento humano, abarcando outras variáveis também importantes, exige a disponibilização e análise de outros indicadores. Para o IDH Global, O Programa das Nações Unidas para o Desenvolvimento reconhece essas limitações e periodicamente propõe metodologias que possam sinalizar tais limitações. Em 2010, por exemplo, foram introduzidos o IDH Ajustado à Desigualdade (IDHAD), que relaciona o IDH ao nível de desigualdade dos países; o Índice de Desigualdade de Gênero (IDG), que reflete desigualdades com base no gênero; e o Índice de Pobreza Multidimensional (IPM), que identifica privações múltiplas em educação, saúde e padrão de vida dos domicílios. No entanto, incluir outras dimensões torna a compreensão e a análise mais complexas, o que dificulta a leitura do índice e compromete o principal objetivo do IDH, que é ser um índice sintético e de fácil compreensão sobre o desenvolvimento humano. Dessa maneira, o IDHM procura manter a coerência com o IDH Global e também enfoca essas três dimensões: saúde, educação e renda – consideradas cruciais para o desenvolvimento humano.
</div>

<div class="text_met" id="18">
    <span><b>É possível haver empate entre municípios na classificação do IDHM? Por quê?</b></span><br>
    Sim. Municípios que apresentam o mesmo valor na média geométrica entre os três componentes do IDHM, considerando três casas decimais, empatam na classificação do IDHM.
</div>

<div class="text_met" id="19">
    <span><b>Como são classificados as UFs, municípios, regiões metropolitanas e UDHs nas faixas de desenvolvimento humano? </b></span><br>

</div>

<div class="text_met">
    <span><b>FAIXAS DE DESENVOLVIMENTO HUMANO MUNICIPAL</b></span><br>
    IDHM entre 0 – 0,499: Muito Baixo Desenvolvimento Humano
    IDHM entre 0,500-0,599: Baixo Desenvolvimento Humano
    IDHM entre 0,600 - 0,699: Médio Desenvolvimento Humano
    IDHM entre 0,700 - 0,799: Alto Desenvolvimento Humano
    IDHM entre 0,800 e 1: Muito Alto Desenvolvimento Humano
</div>

<div class="text_met">
    As faixas de Desenvolvimento Humano Municipal não seguem as faixas do IDH Global. Elas foram adaptadas para contextualizar melhor a realidade brasileira. As faixas do IDH Global são divididas em Baixo, Médio, Alto e Muito Alto Desenvolvimento Humano, e seus valores variam a cada ano, pois são calculadas a partir dos valores mais baixos e mais altos observados nos países. Para o IDHM, elas foram subdivididas em cinco faixas. A faixa "Muito Baixo" do IDHM coincide, de maneira geral, com a faixa "Baixo" do IDH Global. E as faixas "Alto" e "Muito Alto" coincidem com as mesmas faixas do IDH Global. As faixas "Baixo" e "Médio" diferenciam as localidades brasileiras classificadas entre 0,500 e 0,699, de forma a ressaltar as diferenças e reconhecer os esforços de municípios, UFs, regiões metropolitanas e UDHs que estão mais próximos de "Alto" Desenvolvimento Humano.
</div>

<div class="text_met" id="20">
    <span><b>O IDHM é suficiente para medir o nível de desenvolvimento humano de um território?</b></span><br>
    Não. O IDHM pode oferecer uma visão sintética sobre algumas das questões-chave do desenvolvimento humano no município: a saúde (condições para as pessoas viverem uma vida longa e saudável), a educação    (acesso a conhecimento) e a renda (condições de manter um padrão de vida digno). Assim, como qualquer índice, o IDHM não é capaz de abarcar toda a realidade socioeconômica dos territórios (seja uma UF, município, região metropolitana ou UDH), sendo, por isso, importante combiná-lo com outros dados apresentados no Atlas do Desenvolvimento Humano no Brasil, como trabalho, habitação e vulnerabilidade social, para uma visão mais holística e completa sobre as condições de vida em um dado município.
</div>

<div class="text_met">
    Existem também outros aspectos importantes para o desenvolvimento humano que são difíceis de serem mensurados, como a participação social e o empoderamento. O desenvolvimento humano aborda todas essas capacidades do ser humano, mas ainda é um desafio criar índices que possam mensurar toda a sua complexidade.
</div>

<div class="text_met" id="21">
    <span><b>O IDHM é capaz de captar progressos de curto prazo nos territórios?</b></span><br>
    Não. O IDHM indica tendências de longo prazo, mas não é capaz de captar transformações de curto prazo. Isso ocorre devido não somente ao seu vínculo com o Censo Demográfico (que o limita a uma atualização a cada dez anos), mas também pelo uso de variáveis de "estoque", como as de educação da população adulta e de saúde, isto é, indicadores que se movimentam muito lentamente e não captam o progresso e resultados das políticas no curto prazo. Dos indicadores que compõem o IDHM, a renda per capita é o que pode apresentar maior variabilidade a curto prazo, mas deve-se observar que, a partir de determinado nível, essa variabilidade é muito reduzida pela aplicação do logaritmo na construção do índice.
</div>

<div class="text_met" id="22">
    <span><b>O que significa o IDHM do Brasil? Ele é uma média do IDHM de todos os municípios brasileiros?</b></span><br>
    Não. O IDHM do Brasil é calculado a partir de indicadores que são extraídos diretamente da base de dados dos censos, considerando todas as pessoas do país.
    Isto não é a mesma coisa que tirar uma média aritmética simples dos valores dos IDHMs dos 5565 municípios do país, pois, neste caso, em vez de pessoas, estaríamos considerando municípios, e todos entrariam com o mesmo peso no cálculo, seja um município muito grande ou um muito pequeno. Poderia então ser pensado, como alternativa, o cálculo do IDHM do país através da média aritmética ponderada pela população dos municípios. Neste caso, o valor do IDHM se aproximaria bastante do valor obtido pelo método de cálculo adotado para o IDHM do Brasil. Mesmo assim, o resultado não seria ideal. Isto se deve principalmente ao indicador esperança de vida ao nascer, que é obtido de forma indireta e é influenciado por vários fatores, como, por exemplo, a composição etária da população do município.
</div>

<div class="text_met" id="23">
    <span><b>Como foram escolhidos os máximos e mínimos de cada dimensão? Como são calculados?</b></span><br>
    Para a construção do IDHM, todos os indicadores componentes são transformados previamente em índices, que variam entre 0 e 1.
</div>

<div class="text_met">
    Esta transformação do indicador é feita através de uma fórmula matemática simples: (valor observado – valor mínimo) /(valor máximo – valor mínimo). Ela transmite uma ideia também muito simples, que é a de uma relação entre as distâncias do valor observado em relação ao pior e ao melhor valores definidos para ele: quanto mais o valor observado para o município se aproxima do mínimo (ou do pior valor), menor é o numerador da equação e mais próximo de 0 o índice do município vai estar. Por outro lado, quanto mais o valor observado para o município se aproximar do limite máximo (ou melhor valor), mais próximos estarão o numerador e o denominador da equação e, portanto, mais próximo de 1 estará o índice.
</div>

<div class="text_met">
    Desta forma, a escolha dos valores de máximo e mínimo, ou seja, a avaliação do que seriam os limites pior e melhor do indicador em vista, é de fundamental importância e impacta diretamente no valor a ser obtido para o índice. Impacta também na dispersão dos valores do índice entre as diversas localidades.
</div>

<div class="text_met">
    Para o caso do <b>IDHM Educação</b>, a unidade de todos os cinco indicadores considerados no cálculo é o percentual. Desta forma, os valores de máximo e de mínimo são os limites da própria fórmula matemática: respectivamente, 100 e 0.
</div>

<div class="text_met">
    Para o <b>IDHM Longevidade</b>, o único índice é o do indicador esperança de vida ao nascer, calculado considerando os valores de máximo e de mínimo como 85 anos e 25 anos, mesma referência da edição anterior do Atlas.
</div>

<div class="text_met">
    Para o <b>IDHM Renda</b>, o único indicador considerado é a renda per capita. A transformação deste indicador em índice é um pouco mais complexa por vários motivos. Primeiro, como o IDHM é um índice adaptado de um índice para países, há o problema da comparabilidade entre diferentes moedas. No IDH Global, esta questão é resolvida adotando-se um câmbio baseado no poder de compra da cesta básica do país em questão, chamado dólar - paridade-poder-de-compra ($PPC). 
    Em segundo lugar, é necessário considerar a questão da comparabilidade monetária entre vários anos. Esta questão é resolvida adotando-se valores constantes para a moeda, o que é feito aplicando um índice que corrija a inflação do período (no Atlas Brasil é utilizado o Índice Nacional de Preços do Consumidor (INPC) do IBGE. 

</div>

<div class="text_met">
    Finalmente, deve ser considerada a grande dispersão de valores obtidos para a renda <i>per capita</i> entre pessoas e entre localidades. Esta questão é resolvida adotando-se a função logarítmica, que tem a propriedade de ir decrescendo à medida que o número a ser logaritimado cresce. Esta operação traduz também a ideia de rendimentos decrescentes no bem estar, relacionados com o tamanho da renda de cada um. Por exemplo, se a pessoa tem uma renda muito pequena, um acréscimo de 100 reais para ela terá um grande impacto em seu bem-estar, já o mesmo não acontece quando a pessoa já tem a renda muito alta.
</div>

<div class="text_met">
    Os limites de máximo e mínimo definidos para a renda per capita mensal são R$ 4.033,00 e R$ 8,00, respectivamente. O mínimo corresponde ao mínimo adotado pelo IDH Global para países; e o máximo corresponde à renda média do décimo mais rico da população no município mais rico do país (Brasília). A fórmula a ser aplicada para a transformação é, então: (ln valor-ln 8,00)/(ln 4.033,00-ln 8,00).
</div>

<div class="block-01-div-subtitle">O Atlas do Desenvolvimento Humano no Brasil<br /><br /></div>

<div class="text_met" id="24">
    <span><b>O que é o Atlas do Desenvolvimento Humano no Brasil?</b></span><br>
    O <strong>Atlas do Desenvolvimento Humano no Brasil </strong>engloba o Atlas do
    Desenvolvimento Humano nos Municípios e o Atlas do Desenvolvimento Humano nas
    Regiões Metropolitanas. É, uma plataforma de consulta ao Índice de Desenvolvimento
    Humano Municipal (IDHM) de 5.565 municípios brasileiros, 27 Unidades da Federação
    (UF), 20 Regiões Metropolitanas (RM) e suas respectivas Unidades de
    Desenvolvimento Humano (UDH). O Atlas traz, além do IDHM, mais de 200
    indicadores de demografia, educação, renda, trabalho, habitação e vulnerabilidade,
    com dados extraídos dos Censos Demográficos de 1991, 2000 e 2010.Os indicadores podem ser visualizados através de tabelas e mapas, além do Perfil Municipal, que traz análises e informações resumidas, objetivas e amigáveis sobre cada município brasileiro.
</div>

<div class="text_met" id="25">
    <span><b>Quem desenvolveu o Atlas do Desenvolvimento Humano no Brasil?</b></span><br>
    O Atlas do Desenvolvimento Humano no Brasil foi desenvolvido através da parceria entre o Programa das Nações Unidas para o Desenvolvimento (PNUD), o Instituto de Pesquisa Econômica Aplicada (IPEA) e a Fundação João Pinheiro (FJP), com dados dos Censos Demográficos do IBGE.
</div>

<div class="text_met" id="26">
    <span><b>Para que serve o Atlas?</b></span><br>
    O Atlas surge da intenção de estimular a criação de instrumentos eficazes para a observação e análise da realidade brasileira. Sendo assim, o Atlas objetiva ser uma ferramenta de apoio aos gestores e administradores na formulação de políticas públicas adequadas à realidade municipal. Além disso, pretende ser uma plataforma amigável, de fácil acesso e compreensão, disponível a todos os cidadãos brasileiros, de maneira a atingir a sociedade em geral para que ela possa conhecer e transformar a realidade que a cerca.
</div>

<div class="text_met" id="27">
    <span><b>A quem se destina o Atlas do Desenvolvimento Humano no Brasil?</b></span><br>
    O Atlas está disponível para todos os cidadãos brasileiros. Em geral, se destina aos municípios e aos formuladores de políticas e iniciativas voltadas à gestão pública e ao desenvolvimento humano. Sendo assim, pode servir a diversos segmentos sociais e profissionais: administradores e gestores públicos, pesquisadores, estudantes, professores, jornalistas, profissionais da saúde, lideranças comunitárias e de movimentos sociais,  setor privado, terceiro setor, agentes locais de desenvolvimento, entre outros.
</div>

<div class="text_met" id="28">
    <span><b>De onde vêm os dados utilizados no Atlas?</b></span><br>
    Os dados utilizados no Atlas Brasil foram extraídos dos Censos Demográficos de 2010, 2000 e 1991, realizados pelo Instituto Brasileiro de Geografia e Estatística (IBGE).
</div>

<div class="text_met" id="29">
    <span><b>Por que os dados do Atlas e o IDHM são publicados somente a cada 10 anos?</b></span><br>
    O Atlas está vinculado aos dados fornecidos pelos Censos Demográficos realizados de 10 em 10 anos pelo Instituto Brasileiro de Geografia e Estatística (IBGE). Dessa forma, o Atlas Brasil utiliza os dados dos últimos Censos: 2010, 2000 e 1991. A utilização do Censo Demográfico como fonte de dados garante comparabilidade e representatividade entre os municípios, já que os dados foram obtidos através do mesmo processo censitário. Os Censos Demográficos podem ser definidos, segundo as Nações Unidas, como o conjunto das operações que consistem em recolher, agrupar e publicar dados demográficos, econômicos e sociais relativos a um momento determinado ou a certos períodos, a todos os habitantes de um país ou território, ou seja, são um retrato de corpo inteiro do país com o perfil da população e as características de seus domicílios.
</div>

<div class="text_met" id="30">
    <span><b>Quantos municípios estão incluídos no Atlas do Desenvolvimento Humano nos Municípios?</b></span><br>
    Todos os 5.565 municípios brasileiros contabilizados no Censo Demográfico 2010 estão incluídos no Atlas Brasil, com dados também para 2000 e 1991.
</div>

<div class="text_met">
    Para tanto, foi realizada uma compatibilização espacial para os anos de 1991 e de 2000, quando o país tinha, respectivamente, 4.491 e 5.507 municípios. Ou seja, embora não existissem do ponto de vista da divisão político-administrativa do país como município nos anos anteriores, o espaço correspondente a eles em 2010 correspondia a parcelas de um ou mais municípios no passado. Este espaço foi considerado separadamente dos municípios de origem para que pudesse ser feita uma comparação espacial e temporal dos indicadores do Atlas.
</div>

<div class="text_met" id="31">
    <span><b>Meu município não existia em 2000 (ou em 1991), como seus dados estão apresentados no Atlas? O que significa compatibilizar malhas municipais?</b></span><br>
    Em 1991 o país tinha 4.491 municípios, em 2000 tinha 5.507 e em 2010 tinha 5.565 municípios. Ou seja, durante estes 20 anos ocorreram muitas divisões polítco-administrativas e vários novos municípios surgiram. Embora vários municípios não existissem como tal, do ponto de vista da divisão político-administrativa do país nos anos anteriores, o espaço do município atual correspondia a parcelas de um ou mais municípios no passado. Este espaço dos municípios de origem foi considerado separadamente para que pudesse ser feita uma comparação espacial e temporal dos indicadores do Atlas.
</div>

<div class="text_met" id="32">
    <span><b>O que é o Perfil Municipal?</b></span><br>
    O Perfil Municipal é um retrato resumido de cada município brasileiro, acessível online ou exportável em formato PDF. Ele reúne um conjunto de textos e dados comparativos, com informações de 2010, 2000 e 1991. Nele, são apresentados, de forma resumida, os principais indicadores socioeconômicos das áreas de demografia e saúde, educação, habitação, renda, trabalho e vulnerabilidade, além do Índice de Desenvolvimento Humano Municipal (IDHM).
</div>

<div class="text_met" id="33">
    <span><b>Como o Atlas Brasil se relaciona com os Objetivos de Desenvolvimento do Milênio?</b></span><br>
    Em setembro de 2000 a Assembleia Geral da ONU promoveu a Cúpula do Milênio, oportunidade na qual  chefes de estado ou de governo de 191 países pactuaram a Declaração do Milênio. Este documento deu origem a uma série de objetivos para o desenvolvimento e a erradicação da pobreza no mundo, conhecidos como os Objetivos de Desenvolvimento do Milênio (ODM). Para o alcance dos ODM até o ano de 2015, foram criadas diversas metas e indicadores para avaliar e monitorar o progresso dos objetivos acordados.
</div>

<div class="text_met">
    As metas estabelecidas variam de país para país, sendo que cada um deles é responsável por acompanhar o desempenho de seus indicadores. Assim, cada país deve valer-se de suas próprias capacidades sociais e institucionais para monitorá-los. O Atlas do Desenvolvimento Humano no Brasil contribui para a avaliação dos ODM, pois seus dados permitem acompanhar a evolução do bem-estar dos municípios nas últimas duas décadas. 
</div>

<div class="text_met" id="34">
    <span><b>Além do IDHM, que outros dados podem ser encontrados no Atlas?</b></span><br>
    Com dados do Censo 2010, o Atlas também apresenta indicadores de suporte à análise do IDHM.  São mais de 200 indicadores que se dividem em grandes áreas: 
    <ul>
        <li><b>IDHM</b>: IDHM, IDHM Longevidade, IDHM Educação, IDHM Renda;
            <li><b>Demografia e Saúde</b>: esperança de vida ao nascer, mortalidade infantil, taxa de envelhecimento, taxa de fecundidade total;
                <li><b>População</b>: total, por faixa etária, gênero, economicamente ativa;
                    <li><b>Educação</b>: escolaridade, frequência à escola, alfabetização, defasagem idade-ano;
                        <li><b>Habitação</b>: população em domicílios com água encanada, coleta de lixo, energia elétrica;
                            <li><b>Renda</b>: nível/composição, pobreza, desigualdade;
                                <li><b>Trabalho</b>: população ativa, desemprego e características dos ocupados (posição na ocupação, setor de atividade, grau de escolaridade, grau de formalização remuneração e desigualdade de rendimento); 
                                    <li><b>Vulnerabilidade</b>: famílias e pessoas em situação vulnerável quanto à renda, educação, moradia e trabalho.
                                    </ul>
