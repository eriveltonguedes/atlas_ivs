<div class="block-01-div-subtitle">Desenvolvimento Humano, IDH e IDHM<br /><br /></div>

<div class="text_met" id="1">
    <span><b>O que é Desenvolvimento Humano?</b></span><br>
    Difundido no primeiro Relatório de Desenvolvimento Humano Global do Programa das Nações Unidas para o Desenvolvimento (PNUD), em 1990, pelo economista paquistanês Mahbub ul Haq, o conceito de desenvolvimento humano inspira-se nos trabalhos do prêmio Nobel de Economia Amartya Sen e reforça a ideia de que as pessoas são a verdadeira "riqueza das nações". Nessa concepção, desenvolvimento humano é definido como o processo de ampliação das escolhas e liberdades das pessoas para que elas tenham capacidades e oportunidades para serem aquilo que desejam ser. Diferentemente da perspectiva do crescimento econômico, que vê o bem-estar de uma sociedade apenas pelos recursos ou pela renda que ela pode gerar, a abordagem de desenvolvimento humano coloca no centro da discussão as pessoas e suas oportunidades e capacidades.
</div>

<div class="text_met" id="2">
    <span><b>O que é o Índice de Desenvolvimento Humano (IDH)?</b></span><br>
    O Índice de Desenvolvimento Humano (IDH) é uma medida composta de indicadores de saúde, educação e renda. O IDH foi criado em 1990, para o Relatório de Desenvolvimento Humano do Programa das Nações Unidas para o Desenvolvimento, a partir da perspectiva de Amartya Sen e Mahbub ul Haq de que as pessoas são a verdadeira "riqueza das nações", criando uma alternativa às avaliações puramente econômicas de progresso nacional, como o crescimento do Produto Interno Bruto (PIB). O fator inovador do IDH foi a criação de um índice sintético com o objetivo de servir como uma referência para o nível de desenvolvimento humano de uma determinada localidade. O índice varia entre 0 (valor mínimo) e 1 (valor máximo). A composição do IDH compreende indicadores de saúde, educação e renda, pois assume que, para viver vidas que desejam, as pessoas precisam pelo menos ter a possibilidade de levar uma vida longa e saudável, acesso a conhecimento e a oportunidade de desfrutar de um padrão de vida digno.
</div>

<div class="text_met" id="3">
    <span><b>O que é o Índice de Desenvolvimento Humano Municipal (IDHM)?</b></span><br>
    O Brasil foi um dos países pioneiros ao adaptar e calcular o IDH para todos os municípios brasileiros, criando o Índice de Desenvolvimento Humano Municipal (IDHM), em 1998. O IDHM ajusta o IDH para a realidade dos municípios e regiões metropolitanas e reflete as especificidades e desafios regionais no alcance do desenvolvimento humano no Brasil. Para aferir o nível de desenvolvimento humano das unidades federativas (UF), municípios, regiões metropolitanas e Unidades de Desenvolvimento Humano (UDH), as dimensões são as mesmas do IDH Global – saúde, educação e renda –, mas alguns dos indicadores usados são diferentes. O IDHM também varia entre 0 (valor mínimo) e 1 (valor máximo).
</div>

<div class="text_met" id="4">
    <span><b>Para que serve o IDHM?</b></span><br>
    O IDHM é um índice que permite conhecer a realidade do desenvolvimento humano do território brasileiro. Populariza a ideia de que desenvolvimento não se resume à perspectiva do crescimento econômico, mas sim facilita a comparação entre localidades, conduz a um diálogo mais informado na discussão de políticas e estimula a busca por melhores desempenhos socioeconômicos entre os municípios e regiões metropolitanas brasileiras. O índice não abrange todos os aspectos de desenvolvimento humano e não é uma representação da "felicidade" das pessoas, nem indica "o melhor lugar no mundo para se viver", mas sintetiza três das mais importantes dimensões do desenvolvimento humano. Amplia e fomenta o debate, instrumentalizando a sociedade sobre o estado da qualidade de vida nas unidades federativas, municípios, regiões metropolitanas e UDHs brasileiras, estimulando a concertação de atores de forma a protagonizar atividades e políticas inovadoras para a superação dos desafios locais rumo ao desenvolvimento humano.
</div>

